# 依「城市區域大類」上色：維也納 / 湖區 / 尼斯 / 巴黎 / 轉場（合併連續區段，減少 API 請求）
region_color = {
    "vienna": {"red": 0.87, "green": 0.93, "blue": 0.85},
    "lake":   {"red": 0.84, "green": 0.90, "blue": 0.96},
    "nice":   {"red": 1.00, "green": 0.96, "blue": 0.82},
    "paris":  {"red": 0.98, "green": 0.88, "blue": 0.88},
    "trans":  {"red": 0.93, "green": 0.88, "blue": 0.95},
}
def classify(r):
    city = r[3]
    if "→" in city: return "trans"
    if "維也納" in city: return "vienna"
    if "湖區" in city: return "lake"
    if "尼斯" in city or "南法" in city: return "nice"
    if "巴黎" in city: return "paris"
    return "trans"

# 把同區段的連續列合併成一個 range，一次套色
batch = []
start = 2
prev = classify(rows[1])
for i, r in enumerate(rows[2:], start=3):
    cur = classify(r)
    if cur != prev:
        batch.append({"range": f"A{start}:K{i-1}", "format": {"backgroundColor": region_color[prev]}})
        start, prev = i, cur
batch.append({"range": f"A{start}:K{len(rows)}", "format": {"backgroundColor": region_color[prev]}})
ws.batch_format(batch)

